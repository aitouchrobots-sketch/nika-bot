async def broadcast_command(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Рассылка всем пользователям. Только для администраторов."""
    if not update.message or not update.message.from_user:
        return

    sender_id = update.message.from_user.id

    # Первый кто использует /broadcast становится администратором
    if not ADMIN_IDS:
        ADMIN_IDS.add(sender_id)

    if sender_id not in ADMIN_IDS:
        await update.message.reply_text("У вас нет прав для рассылки.")
        return

    text = " ".join(context.args)
    if not text:
        await update.message.reply_text(
            "Использование: /broadcast Ваше сообщение\n\n"
            f"Пользователей в базе: {len(known_users)}"
        )
        return

    sent = 0
    failed = 0
    for user_id in known_users:
        try:
            await context.bot.send_message(chat_id=user_id, text=text)
            sent += 1
        except Exception as e:
            logger.warning(f"Не удалось отправить {user_id}: {e}")
            failed += 1

    await update.message.reply_text(
        f"Рассылка завершена!\n"
        f"Отправлено: {sent}\n"
        f"Не доставлено: {failed}"
    )

async def users_command(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Показывает количество пользователей."""
    if not update.message or not update.message.from_user:
        return
    sender_id = update.message.from_user.id
    if not ADMIN_IDS:
        ADMIN_IDS.add(sender_id)
    if sender_id not in ADMIN_IDS:
        return
    await update.message.reply_text(f"Пользователей в базе: {len(known_users)}")

